package Model;

public class GrupoNutricional {
	
	private String nome;
	private String beneficios;
	
	public GrupoNutricional(String nome, String beneficios) {
		this.nome = nome;
		this.beneficios = beneficios;
	}

	public String getNome() {
		return nome;
	}
	public String getBeneficios() {
		return beneficios;
	}
	
	

}
